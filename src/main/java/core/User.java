package core;

public class User
{
	private String username,password;
	public User()
	{
		
	}
	
	public void setUsername(String name)
	{
		username = name;
	}
	
	public void setPassword(String pw)
	{
		password = pw;
	}
	
	public String getUsername()
	{
		return username;
	}
	
	public String getPassword()
	{
		return password;
	}

}
